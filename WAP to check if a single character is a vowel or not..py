
# Program 1: Check if a character is a vowel or not


# A list of all vowels (lowercase)
vowels = ["a", "e", "i", "o", "u"]

# Ask the user to enter a single character
letter = input("Enter a single character: ")

# .lower() makes it work even if the user types a capital letter
if letter.lower() in vowels:
    print(f"'{letter}' is a vowel!")
else:
    print(f"'{letter}' is NOT a vowel.")


